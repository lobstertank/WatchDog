#!/bin/bash

# Скрипт для запуска Telegram бота из домашней директории
# Используется в cron для автоматического мониторинга

# Переходим в домашнюю директорию пользователя
cd ~/watchdog

# Логируем запуск
echo "$(date): Запуск проверки остатков" >> bot.log

# Запускаем Python скрипт
python3 telegram_bot.py >> bot.log 2>&1

# Логируем завершение
echo "$(date): Проверка завершена" >> bot.log
echo "---" >> bot.log
