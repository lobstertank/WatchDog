#!/bin/bash

# Скрипт для запуска Telegram бота из домашней директории
# Используется в cron для автоматического мониторинга

# Переходим в домашнюю директорию пользователя
cd ~/watchdog

# Создаем директорию логов, если отсутствует
mkdir -p logs

# Логируем запуск
echo "$(date): Запуск проверки остатков" >> logs/bot.log

# Запускаем Python скрипт
python3 launcher.py >> logs/bot.log 2>&1

# Логируем завершение
echo "$(date): Проверка завершена" >> logs/bot.log
echo "---" >> logs/bot.log
